import { Button } from '@/components/ui/button';
import { Checkbox } from '@/components/ui/checkbox';
import {
  Tooltip,
  TooltipContent,
  TooltipTrigger,
} from '@/components/ui/tooltip';
import { ArrowUpDown, Eye, Pencil, Trash2 } from 'lucide-react';

// Factory to create Quran Log columns. We keep it free of hooks; callers should
// pass renderer helpers via options.
export const createColumns = ({
  onView,
  onEdit,
  onDelete,
  onToggleMemo,
  formatPointShort,
  renderPointTooltip,
}) => [
  {
    id: 'select',
    header: ({ table }) => (
      <Checkbox
        aria-label="Select all"
        checked={
          table.getIsAllPageRowsSelected() ||
          (table.getIsSomePageRowsSelected() && 'indeterminate')
        }
        onCheckedChange={(value) => table.toggleAllPageRowsSelected(!!value)}
      />
    ),
    cell: ({ row }) => (
      <Checkbox
        aria-label={`Select log ${row.original.id}`}
        checked={row.getIsSelected()}
        onCheckedChange={(value) => row.toggleSelected(!!value)}
      />
    ),
    enableSorting: false,
    enableHiding: false,
    size: 36,
  },
  {
    accessorKey: 'studentLabel',
    header: ({ column }) => (
      <Button
        className="hover:bg-transparent hover:text-primary text-lg"
        variant="ghost"
        onClick={() => column.toggleSorting(column.getIsSorted() === 'asc')}
      >
        Leerling
        <ArrowUpDown className="ml-2 h-4 w-4" />
      </Button>
    ),
    displayName: 'Leerling',
    size: 260,
  },
  {
    accessorKey: 'from',
    header: ({ column }) => (
      <Button
        className="hover:bg-transparent hover:text-primary text-lg"
        variant="ghost"
        onClick={() => column.toggleSorting(column.getIsSorted() === 'asc')}
      >
        Begin
        <ArrowUpDown className="ml-2 h-4 w-4" />
      </Button>
    ),
    displayName: 'Begin',
    size: 160,
    cell: ({ row }) => {
      const raw = row.getValue('from');
      const short = formatPointShort(raw);
      return (
        <Tooltip>
          <TooltipTrigger asChild>
            <span
              className="inline-block max-w-[140px] truncate align-middle"
              title={short}
            >
              {short}
            </span>
          </TooltipTrigger>
          <TooltipContent
            side="top"
            align="start"
            className="max-w-sm break-words"
          >
            {renderPointTooltip(raw)}
          </TooltipContent>
        </Tooltip>
      );
    },
  },
  {
    accessorKey: 'to',
    header: ({ column }) => (
      <Button
        className="hover:bg-transparent hover:text-primary text-lg"
        variant="ghost"
        onClick={() => column.toggleSorting(column.getIsSorted() === 'asc')}
      >
        Einde
        <ArrowUpDown className="ml-2 h-4 w-4" />
      </Button>
    ),
    displayName: 'Einde',
    size: 160,
    cell: ({ row }) => {
      const raw = row.getValue('to');
      const short = formatPointShort(raw);
      return (
        <Tooltip>
          <TooltipTrigger asChild>
            <span
              className="inline-block max-w-[140px] truncate align-middle"
              title={short}
            >
              {short}
            </span>
          </TooltipTrigger>
          <TooltipContent
            side="top"
            align="start"
            className="max-w-sm break-words"
          >
            {renderPointTooltip(raw)}
          </TooltipContent>
        </Tooltip>
      );
    },
  },
  {
    accessorKey: 'date',
    header: ({ column }) => (
      <Button
        className="hover:bg-transparent hover:text-primary text-lg"
        variant="ghost"
        onClick={() => column.toggleSorting(column.getIsSorted() === 'asc')}
      >
        Datum
        <ArrowUpDown className="ml-2 h-4 w-4" />
      </Button>
    ),
    displayName: 'Datum',
    size: 160,
    cell: ({ row }) => row.getValue('date') || '—',
  },
  {
    accessorKey: 'description',
    header: ({ column }) => (
      <Button
        className="hover:bg-transparent hover:text-primary text-lg"
        variant="ghost"
        onClick={() => column.toggleSorting(column.getIsSorted() === 'asc')}
      >
        Omschrijving
        <ArrowUpDown className="ml-2 h-4 w-4" />
      </Button>
    ),
    displayName: 'Omschrijving',
    size: 320,
    cell: ({ row }) => {
      const value = row.getValue('description') ?? '';
      if (!value) return '—';
      return (
        <span
          className="inline-block max-w-[300px] truncate align-middle"
          title={value}
        >
          {value}
        </span>
      );
    },
  },
  {
    id: 'memorized',
    header: 'Memo',
    size: 90,
    cell: ({ row }) => (
      <Checkbox
        checked={Boolean(row.original.memorized)}
        aria-label="Gememoriseerd"
        onCheckedChange={(v) => onToggleMemo?.(row.original.id, Boolean(v))}
      />
    ),
  },
  {
    id: 'actions',
    size: 120,
    cell: ({ row }) => (
      <div className="flex items-center space-x-2">
        <Button
          variant="ghost"
          className="h-8 w-8 p-0"
          onClick={() => onView(row.original)}
        >
          <span className="sr-only">Bekijken</span>
          <Eye className="h-4 w-4" />
        </Button>
        <Button
          variant="ghost"
          className="h-8 w-8 p-0"
          onClick={() => onEdit(row.original)}
        >
          <span className="sr-only">Bewerken</span>
          <Pencil className="h-4 w-4" />
        </Button>
        <Button
          variant="ghost"
          className="h-8 w-8 p-0"
          onClick={() => onDelete(row.original.id)}
        >
          <span className="sr-only">Verwijderen</span>
          <Trash2 className="h-4 w-4" />
        </Button>
      </div>
    ),
  },
];

export default createColumns;
